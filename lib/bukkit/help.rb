module Bukkit
	def self.help
		puts "  Commands:"
		puts "    Create new server:"
		puts "      bukkit new SERVER_NAME"
		puts "    Start the server:"
		puts "      bukkit start"
		puts "    Install a plugin:"
		puts "      bukkit install PLUGIN_NAME"
		# Put Last
		puts "\n"
		puts "    Show this page:"
		puts "      bukkit --help OR bukkit -h"
		puts "    Get version:"
		puts "      bukkit --version OR bukkit -v"
	end
end